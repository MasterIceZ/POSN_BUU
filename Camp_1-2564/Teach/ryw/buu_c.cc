using ll = long long;

inline void solution(){
	ll d, k, n, b, answer = 0, x;
	priority_queue<ll> pq;
	cin >> d >> k;
	for(ll i=1; i<=d; ++i){
		cin >> n >> b;
		while(n--){
			cin >> x;
			pq.push(x - i * k);
		}
		while(!pq.empty() && b--){
			answer += pq.top() + i * k;
			pq.pop();
		}
	}
	cout << answer;
	return ;
}

signed main(){
	cin.tie(nullptr)->ios::sync_with_stdio(false);	
	int q = 1;
//	cin >> q;
	while(q--){
		solution();
		cout << "\n";
	}
	return 0;
}
