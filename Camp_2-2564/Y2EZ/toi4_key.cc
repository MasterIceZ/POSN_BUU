using ll = long long;

inline void solution(){
	int n, m; 
	string a, b, c, answer = "";
	cin >> n >> m >> a >> b >> c;
	for(auto x: c){
		string now = string(1, x);
		for(int i=0; i<n; ++i){
			now += string(1, a[i]);
			now += string(1, b[i]);
			sort(now.begin(), now.end());
			now = string(1, now[1]);
		}
		answer += now;
	}
	cout << answer;
	return ;
}

signed main(){
	cin.tie(nullptr)->ios::sync_with_stdio(false);	
	int q = 1;
//	cin >> q;
	while(q--){
		solution();
		cout << "\n";
	}
	return 0;
}
